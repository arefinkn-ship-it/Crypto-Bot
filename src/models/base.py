from sqlalchemy import (
    Column, Integer, String, Float, DateTime, Text, 
    Boolean, JSON, DECIMAL, ForeignKey, Index
)
from sqlalchemy.sql import func
from src.core.database import Base


class PriceData(Base):
    """OHLCV price data for all symbols"""
    __tablename__ = 'price_data'
    
    id = Column(Integer, primary_key=True, autoincrement=True)
    symbol = Column(String(20), nullable=False)
    timestamp = Column(DateTime(timezone=True), nullable=False)
    timeframe = Column(String(10), nullable=False)
    
    open = Column(DECIMAL(20, 8))
    high = Column(DECIMAL(20, 8))
    low = Column(DECIMAL(20, 8))
    close = Column(DECIMAL(20, 8))
    volume = Column(DECIMAL(20, 8))
    
    created_at = Column(DateTime(timezone=True), server_default=func.now())
    
    __table_args__ = (
        Index('idx_price_symbol_timeframe', 'symbol', 'timestamp', 'timeframe'),
        Index('idx_price_timestamp', 'timestamp'),
    )


