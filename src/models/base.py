class Signal(Base):
    """Trading signals generated by strategies"""
    __tablename__ = 'signals'
    
    id = Column(Integer, primary_key=True, autoincrement=True)
    symbol = Column(String(20), nullable=False)
    timestamp = Column(DateTime(timezone=True), nullable=False)
    timeframe = Column(String(10), nullable=False)
    
    total_score = Column(DECIMAL(5, 2))
    confidence = Column(String(10))
    
    # Individual strategy scores
    trend_score = Column(DECIMAL(5, 2))
    breakout_score = Column(DECIMAL(5, 2))
    ma_crossover_score = Column(DECIMAL(5, 2))
    mean_reversion_score = Column(DECIMAL(5, 2))
    smc_score = Column(DECIMAL(5, 2))
    
    # Signal details
    direction = Column(String(10))
    entry_price = Column(DECIMAL(20, 8))
    stop_loss = Column(DECIMAL(20, 8))
    take_profit = Column(DECIMAL(20, 8))
    signal_data = Column(JSON)
    
    # ===== FIBONACCI LEVELS =====
    fib_entry_zone = Column(DECIMAL(20, 8))
    fib_stop_loss = Column(DECIMAL(20, 8))
    fib_tp1 = Column(DECIMAL(20, 8))
    fib_tp2 = Column(DECIMAL(20, 8))
    fib_tp3 = Column(DECIMAL(20, 8))
    fib_support = Column(DECIMAL(20, 8))
    fib_resistance = Column(DECIMAL(20, 8))
    fib_retrace_382 = Column(DECIMAL(20, 8))
    fib_retrace_500 = Column(DECIMAL(20, 8))
    fib_retrace_786 = Column(DECIMAL(20, 8))
    fib_swing_high = Column(DECIMAL(20, 8))
    fib_swing_low = Column(DECIMAL(20, 8))
    fib_range = Column(DECIMAL(20, 8))
    
    created_at = Column(DateTime(timezone=True), server_default=func.now())
    
    __table_args__ = (
        Index('idx_signal_symbol_time', 'symbol', 'timestamp'),
        Index('idx_signal_score', 'total_score'),
    )


class BacktestResult(Base):
    """Backtest performance results"""
    __tablename__ = 'backtest_results'
    
    id = Column(Integer, primary_key=True, autoincrement=True)
    strategy_name = Column(String(50), nullable=False)
    symbol = Column(String(20), nullable=False)
    timeframe = Column(String(10), nullable=False)
    
